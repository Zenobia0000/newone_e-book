"""CLI entry for building Python DA/DE Complete Course (13 chapters) decks.

Usage:
  python -m slides_build.build --chapter Ch01
  python -m slides_build.build --all

Each chapter targets 17-28 content slides aligned to teacher_notes/.
Follows teaching-track golden rules (shared/design_system/教學型投影片_黃金守則與泛式.md).
"""
import argparse
import importlib
from datetime import datetime
from pathlib import Path

from . import theme as T


CHAPTERS = {
    "Ch01": ("ch01_deck", "build_ch01", "Ch01_計算機概論與OS角色.pptx"),
    "Ch02": ("ch02_deck", "build_ch02", "Ch02_Python核心與資料結構深化.pptx"),
    "Ch03": ("ch03_deck", "build_ch03", "Ch03_自訂函式與特殊Python函式.pptx"),
    "Ch04": ("ch04_deck", "build_ch04", "Ch04_OOP核心觀念與實例化.pptx"),
    "Ch05": ("ch05_deck", "build_ch05", "Ch05_封裝繼承與魔術方法.pptx"),
    "Ch06": ("ch06_deck", "build_ch06", "Ch06_檔案IO與例外處理.pptx"),
    "Ch07": ("ch07_deck", "build_ch07", "Ch07_OOP_Pandas整合實戰.pptx"),
    "Ch08": ("ch08_deck", "build_ch08", "Ch08_NumPy與向量化思維.pptx"),
    "Ch09": ("ch09_deck", "build_ch09", "Ch09_Pandas_IO與資料清理.pptx"),
    "Ch10": ("ch10_deck", "build_ch10", "Ch10_Pandas轉換_groupby_merge_pivot.pptx"),
    "Ch11": ("ch11_deck", "build_ch11", "Ch11_時間序列與EDA實戰.pptx"),
    "Ch12": ("ch12_deck", "build_ch12", "Ch12_視覺化精華_五種必懂圖.pptx"),
    "Ch13": ("ch13_deck", "build_ch13", "Ch13_Plotly互動與Capstone.pptx"),
}


def _dump_placeholders_yaml(code: str, registry: list, out_path: Path):
    lines = [
        f"# Auto-generated by slides_build.build for {code}",
        f"# {datetime.now().isoformat(timespec='seconds')}",
        "",
        f"chapter: {code}",
        f"total: {len(registry)}",
        f"pending: {sum(1 for r in registry if r['status'] == 'pending')}",
        "placeholders:",
    ]
    for r in registry:
        bb = r["bounding_box"]
        lines.extend([
            f"  - id: {r['id']}",
            f"    slot_name: \"{r['slot_name']}\"",
            f"    description: \"{r['description']}\"",
            f"    source_url: \"{r['source_url']}\"",
            f"    recommended_size: \"{r['recommended_size']}\"",
            f"    bounding_box: {{x: {bb['x']}, y: {bb['y']}, w: {bb['w']}, h: {bb['h']}}}",
            f"    status: {r['status']}",
            f"    asset_path: {r['asset_path'] or 'null'}",
        ])
    out_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def build_chapter(code: str) -> Path:
    if code not in CHAPTERS:
        raise ValueError(f"unknown chapter {code!r}; known: {list(CHAPTERS)}")
    module_name, func_name, filename = CHAPTERS[code]
    module = importlib.import_module(f".slides.{module_name}", package="slides_build")
    builder = getattr(module, func_name)
    out = T.OUTPUT_DIR / filename
    registry: list = []
    builder(out, image_registry=registry)

    if registry:
        yaml_path = T.OUTPUT_DIR / f"_image_placeholders_{code}.yaml"
        _dump_placeholders_yaml(code, registry, yaml_path)
        print(f"    ↳ placeholders audit: {yaml_path} ({len(registry)} pending)")

    return out


def main():
    ap = argparse.ArgumentParser()
    g = ap.add_mutually_exclusive_group(required=True)
    g.add_argument("--chapter", choices=list(CHAPTERS))
    g.add_argument("--all", action="store_true", help="build all 13 chapters")
    args = ap.parse_args()

    targets = list(CHAPTERS.keys()) if args.all else [args.chapter]
    for code in targets:
        try:
            out = build_chapter(code)
            print(f"  ✓ {code}  →  {out}")
        except Exception as e:
            print(f"  ✗ {code}  FAILED: {e}")


if __name__ == "__main__":
    main()
